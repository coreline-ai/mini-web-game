def rel(path: Path) -> str:
    return str(path.relative_to(ROOT))


def copy_sheets() -> dict[str, Path]:
    SHEETS.mkdir(parents=True, exist_ok=True)
    copied = {}
    for name, source in SOURCE_SHEETS.items():
        if not source.exists():
            raise FileNotFoundError(f"missing generated sheet: {source}")
        dest = SHEETS / f"{name}_sheet.png"
        if source.resolve() != dest.resolve():
            shutil.copy2(source, dest)
        copied[name] = dest
    return copied


def corner_bg(im: Image.Image) -> tuple[int, int, int]:
    rgb = im.convert("RGB")
    w, h = rgb.size
    patch = max(4, min(w, h) // 24)
    pixels = []
    for box in [(0,0,patch,patch),(w-patch,0,w,patch),(0,h-patch,patch,h),(w-patch,h-patch,w,h)]:
        crop = rgb.crop(box)
        pixels.extend(crop.getdata())
    return tuple(sorted(p[i] for p in pixels)[len(pixels)//2] for i in range(3))


def edge_bg_palette(im: Image.Image, sample_count: int = 96) -> list[tuple[int, int, int]]:
    rgb = im.convert("RGB")
    w, h = rgb.size
    pixels = []
    for i in range(sample_count):
        x = round(i * (w - 1) / max(1, sample_count - 1))
        y = round(i * (h - 1) / max(1, sample_count - 1))
        pixels.append(rgb.getpixel((x, 0)))
        pixels.append(rgb.getpixel((x, h - 1)))
        pixels.append(rgb.getpixel((0, y)))
        pixels.append(rgb.getpixel((w - 1, y)))
    # Keep representative edge colors. Quantizing avoids thousands of distance checks while still
    # handling gradient backgrounds better than a single corner median.
    seen: dict[tuple[int, int, int], tuple[int, int, int]] = {}
    for r, g, b in pixels:
        key = (r // 12, g // 12, b // 12)
        seen.setdefault(key, (r, g, b))
    return list(seen.values()) or [corner_bg(rgb)]


def remove_bg_flood(im: Image.Image, tolerance: int = 34) -> Image.Image:
    rgba = im.convert("RGBA")
    rgb = rgba.convert("RGB")
    w, h = rgb.size
    px = rgb.load()
    bg = corner_bg(rgb)
    tol_sq = tolerance * tolerance * 3

    def close(x: int, y: int) -> bool:
        r, g, b = px[x, y]
        return (r-bg[0])**2 + (g-bg[1])**2 + (b-bg[2])**2 <= tol_sq

    seen = bytearray(w*h)
    q = deque()
    def add(x: int, y: int) -> None:
        i = y*w+x
        if not seen[i] and close(x,y):
            seen[i] = 1
            q.append((x,y))
    for x in range(w):
        add(x,0); add(x,h-1)
    for y in range(h):
        add(0,y); add(w-1,y)
    while q:
        x,y=q.popleft()
        if x+1<w: add(x+1,y)
        if x-1>=0: add(x-1,y)
        if y+1<h: add(x,y+1)
        if y-1>=0: add(x,y-1)
    alpha = Image.frombytes("L", (w,h), bytes(0 if v else 255 for v in seen))
    alpha = alpha.filter(ImageFilter.GaussianBlur(0.55)).point(lambda a: 0 if a < 18 else (255 if a > 238 else a))
    rgba.putalpha(alpha)
    return rgba


def contain(im: Image.Image, size: tuple[int,int], margin: float) -> Image.Image:
    rgba = im.convert("RGBA")
    bbox = rgba.getbbox()
    if bbox:
        rgba = rgba.crop(bbox)
    tw, th = size
    max_w = max(1, round(tw*(1-margin*2)))
    max_h = max(1, round(th*(1-margin*2)))
    scale = min(max_w/rgba.width, max_h/rgba.height)
    resized = rgba.resize((max(1, round(rgba.width*scale)), max(1, round(rgba.height*scale))), Image.Resampling.LANCZOS)
    out = Image.new("RGBA", size, (0,0,0,0))
    out.alpha_composite(resized, ((tw-resized.width)//2, (th-resized.height)//2))
    return out


def remove_small_alpha_components(im: Image.Image, min_ratio: float = 0.045) -> Image.Image:
    """Drop disconnected alpha specks left by sheet crops/background removal.

    GPT image sheets sometimes include small UI fragments below or beside the main subject.
    If we `contain()` those fragments, the runtime sprite appears broken because the main
    parcel/chute is scaled around invisible composition junk. Keep the dominant connected
    component and any substantial attached pieces; remove tiny disconnected leftovers.
    """
    rgba = im.convert("RGBA")
    alpha = rgba.getchannel("A")
    w, h = alpha.size
    mask = alpha.point(lambda a: 1 if a > 24 else 0)
    data = bytearray(mask.tobytes())
    seen = bytearray(w * h)
    components: list[list[int]] = []

    for start, v in enumerate(data):
        if not v or seen[start]:
            continue
        q = deque([start])
        seen[start] = 1
        comp: list[int] = []
        while q:
            idx = q.popleft()
            comp.append(idx)
            x = idx % w
            y = idx // w
            for nx, ny in ((x + 1, y), (x - 1, y), (x, y + 1), (x, y - 1)):
                if nx < 0 or nx >= w or ny < 0 or ny >= h:
                    continue
                ni = ny * w + nx
                if data[ni] and not seen[ni]:
                    seen[ni] = 1
                    q.append(ni)
        components.append(comp)

    if len(components) <= 1:
        return rgba
    max_area = max(len(comp) for comp in components)
    keep = bytearray(w * h)
    for comp in components:
        if len(comp) >= max_area * min_ratio:
            for idx in comp:
                keep[idx] = 255
    cleaned_alpha = Image.frombytes("L", (w, h), bytes(a if keep[i] else 0 for i, a in enumerate(alpha.tobytes())))
    rgba.putalpha(cleaned_alpha)
    return rgba


def cover(im: Image.Image, size: tuple[int,int]) -> Image.Image:
    rgba = im.convert("RGBA")
    tw, th = size
    scale = max(tw/rgba.width, th/rgba.height)
    resized = rgba.resize((round(rgba.width*scale), round(rgba.height*scale)), Image.Resampling.LANCZOS)
    left = (resized.width-tw)//2
    top = (resized.height-th)//2
    out = resized.crop((left, top, left+tw, top+th))
    opaque = Image.new("RGBA", size, (0,0,0,255))
    opaque.alpha_composite(out)
    return opaque


def provenance(task: Task, sheet_path: Path, raw_path: Path, generated_at: str) -> dict:
    digest_source = f"{task.id}|{task.sheet}|{task.crop}|{sheet_path.name}|{METHOD}"
    return {
        "source": "generated-for-game",
        "generatedFor": GAME_ID,
        "generator": GENERATOR,
        "method": METHOD,
        "model": MODEL,
        "sourceSkill": "imagegen",
        "toolMode": "built-in-image_gen",
        "sourceSheet": rel(sheet_path),
        "rawPath": rel(raw_path),
        "cropBox": list(task.crop),
        "promptHash": hashlib.sha256(digest_source.encode()).hexdigest()[:16],
        "quality": "high",
        "generatedAt": generated_at,
        "postprocess": task.postprocess,
    }


def upsert(entries: list[dict], new_entry: dict) -> None:
    for i, entry in enumerate(entries):
        if entry.get("id") == new_entry["id"]:
            merged = dict(entry)
            merged.update(new_entry)
            entries[i] = merged
            return
    entries.append(new_entry)


def main() -> int:
    sheets = copy_sheets()
    generated_at = dt.datetime.now(dt.timezone.utc).isoformat(timespec="seconds")
    manifest = json.loads(MANIFEST.read_text(encoding="utf-8"))
    manifest.setdefault("images", [])
    manifest.setdefault("stageBackgrounds", [])

    for task in TASKS:
        sheet_path = sheets[task.sheet]
        sheet = Image.open(sheet_path).convert("RGBA")
        crop = sheet.crop(task.crop)
        raw_path = RAW / task.output
        out_path = PROD / task.output
        raw_path.parent.mkdir(parents=True, exist_ok=True)
        out_path.parent.mkdir(parents=True, exist_ok=True)
        crop.save(raw_path, optimize=True)
        if task.postprocess == "cover":
            final = cover(crop, task.canvas)
        else:
            cleaned = remove_bg_flood(crop, task.tolerance)
            if task.role in {"parcel", "sort-bin"}:
                cleaned = remove_small_alpha_components(cleaned)
            final = contain(cleaned, task.canvas, task.margin)
        final.save(out_path, optimize=True)

        entry = {
            "id": task.id,
            "path": rel(out_path),
            "type": task.asset_type,
            "role": task.role,
            "quality": QUALITY,
            "minWidth": task.canvas[0],
            "minHeight": task.canvas[1],
            "requiresAlpha": task.requires_alpha,
            "provenance": provenance(task, sheet_path, raw_path, generated_at),
        }
        upsert(manifest["images"], entry)
        if task.role == "stage-background":
            stage_entry = dict(entry)
            stage_entry["type"] = "stage-background"
            stage_entry["label"] = task.id.replace("_", " ")
            upsert(manifest["stageBackgrounds"], stage_entry)
        print(f"[integrated] {task.id}: {rel(raw_path)} -> {rel(out_path)}")

    manifest["qualityTier"] = QUALITY
    manifest["imagegen"] = {
        "model": MODEL,
        "method": METHOD,
        "sourceSkill": "imagegen",
        "requiredImageCount": len(TASKS),
        "lastFullIntegrationAt": generated_at,
        "sheets": {name: rel(path) for name, path in sheets.items()},
    }
    MANIFEST.write_text(json.dumps(manifest, ensure_ascii=False, indent=2) + "\n", encoding="utf-8")
    return 0

if __name__ == "__main__":
    raise SystemExit(main())
